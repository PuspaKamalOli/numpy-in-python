import numpy as np

a_1 = np.array([[1, 2, 3],
                [3, 4, 5]])
a_2 = np.array([[3, 4, 5],
                [6, 7, 8]])
# sum
print(a_1 + a_2)
# subtraction
print(a_2 - a_1)
# multiplication
print(a_1 * a_2)
# division
print(a_1 / a_2)
# floor division
print(a_1 // a_2)
# remainder
print(a_1 % a_2)
# raise power
print(a_1 ** a_2)
